    public function store(Request $request)
    {
       // dd($request->all());
        $slug = $this->getSlug($request);

        $dataType = Voyager::model('DataType')->where('slug', '=', $slug)->first();

        // Check permission
        $this->authorize('add', app($dataType->model_name));

        // Validate fields with ajax
        $val = $this->validateBread($request->all(), $dataType->addRows)->validate();
        $data = $this->insertUpdateData($request, $slug, $dataType->addRows, new $dataType->model_name());

        event(new BreadDataAdded($dataType, $data));
        $this->updateProductCategories($request,$data->id);
        // if($request->category)//checking if the request has categories
        // {
        //     foreach ($request->category as $category) {
        //         Category_Product::create(['Product_id'=>$data->id,'Category_id'=>$category]);//also got to category_product and make product_id and category_id fillable
        //     }
        // }

        if (!$request->has('_tagging')) {
            if (auth()->user()->can('browse', $data)) {
                $redirect = redirect()->route("voyager.{$dataType->slug}.index");
            } else {
                $redirect = redirect()->back();
            }

            return $redirect->with([
                'message'    => __('voyager::generic.successfully_added_new')." {$dataType->getTranslatedAttribute('display_name_singular')}",
                'alert-type' => 'success',
            ]);
        } else {
            return response()->json(['success' => true, 'data' => $data]);
        }
    }

   

   
    //proteted so that it cannot be accesed from outside the class
    protected function updateProductCategories(Request $request,$id){
        if($request->category)//checking if the request has categories
        {
            foreach ($request->category as $category) {
                Category_Product::create(['Product_id'=>$id,'Category_id'=>$category]);//also got to category_product and make product_id and category_id fillable
            }
        }

    }
